    private void clean() {
        tf_id.setText("");
        tf_name.setText("");
        tf_purchase_price.setText("");
        tf_sale_price.setText("");
        tf_quantity.setText("");
    }

    private void table_update() {
        List<Map<String, Object>> datos = bd.leer(configuration, tabla);
        DefaultTableModel model = (DefaultTableModel) tbl_inventory.getModel();
        model.setRowCount(0);
        for (Map<String, Object> fila : datos) {
            model.addRow(new Object[] {
                    fila.get("id"),
                    fila.get("name"),
                    fila.get("purchase_price"),
                    fila.get("sale_price"),
                    fila.get("quantity"),
            });
        }
    }
    
    private void delete(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete
        // TODO add your handling code here:
        int id = Integer.parseInt(tf_id.getText());
        bd.borrar(id, configuration, tabla);
        
        clean();
        table_update();
    }//GEN-LAST:event_delete

    private void read(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_read
        // TODO add your handling code here:
        List<Map<String, Object>> datos = bd.leer(configuration, tabla);
        DefaultTableModel model = (DefaultTableModel) tbl_inventory.getModel();
        model.setRowCount(0);
        for (Map<String, Object> fila : datos) {
            model.addRow(new Object[] {
                    fila.get("id"),
                    fila.get("name"),
                    fila.get("purchase_price"),
                    fila.get("sale_price"),
                    fila.get("quantity"),
            });
        }
    }//GEN-LAST:event_read

    private void update(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update
        // TODO add your handling code here:
        int id = Integer.parseInt(tf_id.getText());
        Map datos = new HashMap();
        datos.put(campos[0], tf_name.getText());
        datos.put(campos[1], tf_purchase_price.getText());
        datos.put(campos[2], tf_sale_price.getText());
        datos.put(campos[3], tf_quantity.getText());
        
        bd.actualizar(id, datos, configuration, tabla);
        
        clean();
        table_update();
    }//GEN-LAST:event_update

    private void create(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create
        // TODO add your handling code here:
        Map datos = new HashMap();
        datos.put(campos[0], tf_name.getText());
        datos.put(campos[1], tf_purchase_price.getText());
        datos.put(campos[2], tf_sale_price.getText());
        datos.put(campos[3], tf_quantity.getText());
        
        bd.crear(datos, configuration, tabla);
        
        clean();
        table_update();
    }//GEN-LAST:event_create

