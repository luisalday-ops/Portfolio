    private void table_update() {
        List<Map<String, Object>> datos = bd.leer(configuration, tabla);
        DefaultTableModel model = (DefaultTableModel) tbl_tienda.getModel();
        model.setRowCount(0);
        for (Map<String, Object> fila : datos) {
            model.addRow(new Object[] {
                    fila.get("id"),
                    fila.get("name"),
                    fila.get("quantities_sold"),
                    fila.get("unit_price"),
                    fila.get("date_sale"),
                    fila.get("total"),
            });
        }
    }
    
